public class Main {
  String fname = "Jaya";
  String lname = "Deep";
  int age = 19;

  public static void main(String[] args) {
    Main myObj = new Main();
    System.out.println("Name: " + myObj.fname + " " + myObj.lname);
    System.out.println("Age: " + myObj.age);
  }
}

/* Output : 
Name: Jaya Deep
Age: 19 */
